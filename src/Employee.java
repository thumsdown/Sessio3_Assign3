
public class Employee {
	
	private String EmpName;
	private String EmpID;
	
	public String getEmployeeName()
	{
		return EmpName;
	}
	
	public String getEmployeeId()
	{
		return EmpID;
	}
	
	public void setEmployeeName(String Name )
	{
		this.EmpName = Name;
	}
	
	public void setEmployeeId( String Id)
	{
		this.EmpID = Id;
	}

}
